const dictionaries: Record<Language, Dict> = { pt, en };

function format(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) =>
    vars[k] !== undefined ? String(vars[k]) : `{${k}}`
  );
}

export function translate(
  key: string,
  lang: Language,
  vars?: Record<string, string | number>
): string {
  const dict = dictionaries[lang] ?? dictionaries.pt;
  const raw = dict[key] ?? dictionaries.pt[key] ?? key;
  return vars ? format(raw, vars) : raw;
}

export function useT() {
  const language = useAppStore((s) => s.settings.language);
  return (key: string, vars?: Record<string, string | number>) =>
    translate(key, language, vars);
}

export function useLanguage(): Language {
  return useAppStore((s) => s.settings.language);
}
